#include "Exercice 32.h"
struct Livres {
	char titre[50];
	char auteur[50];
	char sujet[100];
	int livre_id;
};

void imprimerLivre(struct Livres *livre);

int main() {
	struct Livres livre1;
	struct Livres livre2;

	strcpy(livre1.titre, "Programation C");
	strcpy(livre1.auteur, "Nuha Ali");
	strcpy(livre1.sujet, "Tutoriel de programation C");
	livre1.livre_id = 6495407;

	strcpy(livre2.titre, "Facturation telecom");
	strcpy(livre2.auteur, "Zara Ali");
	strcpy(livre2.sujet, "Didacticiel de facturation telecom");
	livre2.livre_id = 6495700;

	imprimerLivre(&livre1);
	imprimerLivre(&livre2);

	return 0;
}

void imprimerLivre(struct Livres *livre) {
	printf("Livre titre : %s\n", livre->titre);
	printf("Livre auteur : %s\n", livre->auteur);
	printf("Livre sujet : %s\n", livre->sujet);
	printf("Livre livre_id : %d\n", livre->livre_id);
}